/* Seed documents and templates.
   Authored as arrays of single-quoted lines joined with '\n': backticks are
   literal inside single quotes (so ``` fences are safe) and every LaTeX
   backslash is doubled so the source survives JS string parsing. Verified by a
   render pass in the build/selftest. */

/* Seed refresh: when a seed doc changes, existing libraries should get the new
   text — but ONLY if the stored copy was never edited. "Never edited" is
   detected by hashing the stored content against every historical version of
   that seed (hashes below; regenerate by hashing WELCOME_MD etc. from each
   git version of this file). An edited seed doc is never touched. */
export function seedHash(s) {
  let h = 5381;
  for (let i = 0; i < s.length; i++) h = ((h << 5) + h + s.charCodeAt(i)) >>> 0;
  return h.toString(36);
}
